import { existsSync, mkdirSync, writeFileSync } from 'fs';
import { join, dirname } from 'path';
import { fileURLToPath } from 'url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const GENERATED_DIR = join(__dirname, '..', 'src', 'data', 'generated');
const OUTPUT_FILE = join(GENERATED_DIR, 'fallback-examples.js');

const RAW_BASE = 'https://raw.githubusercontent.com/Randware/Wasome/main/';
const SINGLE_FILE_PATH = 'docs/examples/single_file';

async function main() {
    if (existsSync(OUTPUT_FILE)) {
        console.log('[fetch-examples] Fallback examples already exist, skipping.');
        return;
    }

    console.log('[fetch-examples] Fetching fallback examples from GitHub...');

    try {
        // Only fetch the essential single-file examples (originally hardcoded fallbacks)
        const ESSENTIAL_FILES = ['enum', 'if', 'loop', 'operator', 'struct'];
        const examples = {};

        for (const key of ESSENTIAL_FILES) {
            const url = `${RAW_BASE}${SINGLE_FILE_PATH}/${key}.waso`;
            console.log(`  Fetching ${key}.waso...`);

            const fileRes = await fetch(url);
            if (!fileRes.ok) {
                console.warn(`  Warning: Failed to fetch ${key}.waso (${fileRes.status}), skipping.`);
                continue;
            }

            examples[key] = await fileRes.text();
        }

        // Build the fallback tree
        const fallbackTree = [
            {
                name: 'single_file',
                path: 'docs/examples/single_file',
                type: 'dir',
                children: Object.keys(examples).sort().map(key => ({
                    name: `${key}.waso`,
                    path: `docs/examples/single_file/${key}.waso`,
                    type: 'file',
                    download_url: `${RAW_BASE}docs/examples/single_file/${key}.waso`
                }))
            },
            {
                name: 'single_project',
                path: 'docs/examples/single_project',
                type: 'dir',
                children: null
            },
            {
                name: 'multi-project',
                path: 'docs/examples/multi-project',
                type: 'dir',
                children: null
            }
        ];

        // Write output as a JS module
        mkdirSync(GENERATED_DIR, { recursive: true });
        const jsContent = `// Auto-generated by scripts/fetch-examples.js — do not edit manually
export const examples = ${JSON.stringify(examples, null, 2)};

export const tree = ${JSON.stringify(fallbackTree, null, 2)};
`;
        writeFileSync(OUTPUT_FILE, jsContent);

        console.log(`[fetch-examples] Saved ${Object.keys(examples).length} files to ${OUTPUT_FILE}`);
    } catch (err) {
        console.error('[fetch-examples] Failed to fetch examples:', err.message);
        console.error('[fetch-examples] The website will still work but without offline fallback examples.');
        process.exit(0); // Don't fail the build
    }
}

main();
